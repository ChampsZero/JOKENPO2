function batalha(carta1, carta2) {
    let elemento1 = carta1.elemento;
    let valor1 = carta1.valor;
    let elemento2 = carta2.elemento;
    let valor2 = carta2.valor;

    let vantagem1 = vantagens[elemento1].includes(elemento2);
    let vantagem2 = vantagens[elemento2].includes(elemento1);

    let valorFinal1 = valor1;
    let valorFinal2 = valor2;

    let resultado = '';
    let vantagem = '';
    let vencedor = '';

    // Verificar vantagem de elemento
    if (vantagem1 && !vantagem2) {
        valorFinal1 *= 1.5;
        vantagem = `${elemento1} tem vantagem sobre ${elemento2}`;
    } else if (vantagem2 && !vantagem1) {
        valorFinal2 *= 1.5;
        vantagem = `${elemento2} tem vantagem sobre ${elemento1}`;
    }

    // Comparar valores finais
    if (valorFinal1 > valorFinal2) {
        resultado = `${elemento1} vence com valor ${valorFinal1.toFixed(1)} contra ${valorFinal2.toFixed(1)} de ${elemento2}`;
        vencedor = 'jogador';
    } else if (valorFinal2 > valorFinal1) {
        resultado = `${elemento2} vence com valor ${valorFinal2.toFixed(1)} contra ${valorFinal1.toFixed(1)} de ${elemento1}`;
        vencedor = 'bot';
    } else {
        resultado = `Empate! Ambos ${elemento1} e ${elemento2} têm valor ${valorFinal1.toFixed(1)}`;
        vencedor = 'empate';
    }
    
    return { resultado, vantagem, vencedor };
}

