function iniciarBatalha() {
    let cartaBot = maoBot[0];
    
    document.getElementById('cartaBotBatalha').src = cartaBot.imagem;
    
    let resultadoBatalha = batalha(cartaSelecionada, cartaBot);
    
    document.getElementById('resultadoBatalha').textContent = resultadoBatalha.resultado;
    
    let vantagemElemento = document.getElementById('vantagemElemento');
    if (resultadoBatalha.vantagem) {
        vantagemElemento.textContent = resultadoBatalha.vantagem;
        vantagemElemento.style.display = 'block';
    } else {
        vantagemElemento.style.display = 'none';
    }
    
    if (resultadoBatalha.vencedor === 'jogador') {
        pontosJogador += 1;
    } else if (resultadoBatalha.vencedor === 'bot') {
        pontosBot += 1;
    }
    
    atualizarPlacar();
    
    document.getElementById('botaoBatalha').style.display = 'none';
    
    maoBot.shift();
    
    setTimeout(() => {
        finalizarRodada();
    }, 3000);
}

